public class CD {
    private String maSp, tieuDe, loai;
    private int namXB;

    public CD(String maSp, String tieuDe, String loai, int namXB) {
        this.maSp = maSp;
        this.tieuDe = tieuDe;
        this.loai = loai;
        this.namXB = namXB;
    }

    public String getMaSp() {
        return maSp;
    }

    public void setMaSp(String maSp) {
        this.maSp = maSp;
    }

    public String getTieuDe() {
        return tieuDe;
    }

    public void setTieuDe(String tieuDe) {
        this.tieuDe = tieuDe;
    }

    public String getLoai() {
        return loai;
    }

    public void setLoai(String loai) {
        this.loai = loai;
    }

    public int getNamXB() {
        return namXB;
    }

    public void setNamXB(int namXB) {
        this.namXB = namXB;
    }
    
    
}
